package Array;

public class Array {
    public static void main(String[] args) {

        Integer array[] = {10, 50, 30, 52, 30}; //Nosso array
        Integer n = 0; //variavel para percorrer a index

        //while para percorrer todas index
        while (n <= 4){
            System.out.println(array[n]);
            n ++;
        }


    }
}
