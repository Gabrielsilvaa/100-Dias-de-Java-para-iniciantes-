package operadores;

public class Operadoreslogicos {

    public static void main(String[] args) {

        // && - operador E é delcador com E comercial
        // || - operador OU e declarado com ||
        // ! - operador NOT e declarado com !

        //Com o operador lógico E (&&) testamos se dois booleanos são true.
        System.out.println(true&&true);

        //O operador OU (||) testa se pelo menos um dentre dois booleanos é true.
        System.out.println(false||true);

        //NOT (!) nega um booleano, que é equivalente a inverter o seu valor
        System.out.println(!true + " not");


    }

}
