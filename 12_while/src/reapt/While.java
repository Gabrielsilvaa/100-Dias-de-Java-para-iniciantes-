package reapt;

public class While {
    public static void main(String[] args) {

        //definimos uma variavel contador
        Integer cont = 0;

        //Iniciamos nosso bloco de repetição e passamos uma condição para ele executar
        while (cont <=10){
            //imprimimos o valor da variavel para acompanhar sua repetição
            System.out.println(cont);
            //adiciona mais um
            cont++;
        }

    }
}
